package com.test.beans.coffee;

/**
 * @author kkanaparthi
 *
 */
public class CoffeeBean {

	/**
	 * @return the coffeeName
	 */
	public String getCoffeeName() {
		return coffeeName;
	}
	/**
	 * @param coffeeName the coffeeName to set
	 */
	public void setCoffeeName(String coffeeName) {
		this.coffeeName = coffeeName;
	}
	/**
	 * @return the coffeeType
	 */
	public String getCoffeeType() {
		return coffeeType;
	}
	/**
	 * @param coffeeType the coffeeType to set
	 */
	public void setCoffeeType(String coffeeType) {
		this.coffeeType = coffeeType;
	}
	/**
	 * @return the coffeeId
	 */
	public String getCoffeeId() {
		return coffeeId;
	}
	/**
	 * @param coffeeId the coffeeId to set
	 */
	public void setCoffeeId(String coffeeId) {
		this.coffeeId = coffeeId;
	}
	/**
	 * @return the coffeeSize
	 */
	public String getCoffeeSize() {
		return coffeeSize;
	}
	/**
	 * @param coffeeSize the coffeeSize to set
	 */
	public void setCoffeeSize(String coffeeSize) {
		this.coffeeSize = coffeeSize;
	}
	private String coffeeName;
	private String coffeeType;
	private String coffeeId;
	private String coffeeSize;
	
}
